/**
 * Normalize emoji by removing variation selectors (U+FE0E text, U+FE0F emoji)
 * Many platforms add these invisible characters to force presentation style.
 * @param emoji - Unicode emoji character (possibly with variation selector)
 * @returns Normalized emoji without variation selectors
 */
function normalizeEmoji(emoji: string): string {
  // Remove variation selectors U+FE0E (text) and U+FE0F (emoji)
  return emoji.replaceAll(/[\uFE0E\uFE0F]/g, '');
}

/**
 * Get the full URL for an animated emoji
 * @param emoji - Unicode emoji character
 * @returns Full URL to animated webp, or null if no animated version exists
 */
export function getAnimatedEmojiUrl(emoji: string): string | null {
  const normalized = normalizeEmoji(emoji);
  const path = ANIMATED_EMOJI_MAP[normalized];
  if (!path) return null;
  return `${TELEGRAM_BASE_URL}/${path}`;
}

/**
 * Check if an emoji has an animated version
 * @param emoji - Unicode emoji character
 * @returns true if animated version exists
 */
export function hasAnimatedEmoji(emoji: string): boolean {
  const normalized = normalizeEmoji(emoji);
  return ANIMATED_EMOJI_MAP[normalized] !== null && ANIMATED_EMOJI_MAP[normalized] !== undefined;
}

/**
 * Get all emojis that have animated versions
 * @returns Array of emoji characters that have animated versions
 */
export function getAnimatedEmojis(): string[] {
  return Object.entries(ANIMATED_EMOJI_MAP)
    .filter(([, path]) => path !== null)
    .map(([emoji]) => emoji);
}

